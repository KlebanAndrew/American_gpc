<h2>Чим хороша "AC Solar Panel or Micro Inverter" панель?</h2>

<p>Існує ряд складнощів, викликаних традиційним способом підключення сонячних
	панелей одна з одною, які мікро-інвертори можуть допомогти подолати, включаючи:</p>

<h3>Високу напругу постійного струму</h3>

<p>Висока напруга постійного струму може створити ризик дуже високої температури електричної дуги і потенційно пожежі.
	Тому що, мікро-інвертори перетворюються в змінний потенціал, для того, щоб стати значно мінімізованими.</p>

<h3>Розподільчий пристрій</h3>

<p>Висока напруга постійного струму вимагає дорогих захисних вимикачів і запобіжників.
	За допомогою змінного струму, розподільчий пристрій є найбільш доступними і, отже, дешевшим.</p>

<h3>Затінення</h3>

<p>Коли сонячні панелі з'єднані один з одним в SERIES STRING, тільки один з них затінений може істотно вплинути на всю кількість.<br /> <br />

	Як приклад: Ось масив з 3 сонячних панелей, з'єднаних зі звичайним, центральним інвертором. На одну з панелей «наклали» птиці ,
	а це може легко зменшити обсяги виробництва на 50%.
	Тим не менш, він може також скоротити виробництво і будь-якої іншої панелі на ту ж суму.</p>
<br /><br />

