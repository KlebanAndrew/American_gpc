<p>За даними американської Національної лабораторії поновлюваних джерел енергії, цей ефект може дати аж на 12% більше енергії.</p>

<h3>Розбіжність панелів сонячних батерей</h3>

<p>Коли сонячні панелі виготовлені, то кожен з них мають по кілька різних електричних характеристик через розбіжність виробничих допусків.
	При підключенні їх в рядок серії, цей ефект називається "розбіжністю". Мікро-Інвертори можуть адаптуватися до індивідуальних
	особливостей кожної панелі, щоб уникнути цих невідповідності.</p>

<h3>Відстеження точки максимальної потужності</h3>

<p>Як ефект розбіжності, різні електричні характеристики можуть також створювати різні максимальні значення потужності точки для кожної
	панелі сонячних батарей. Максимальне значення потужності, є ідеальним моментом для вилучення максимальної потужності з панелі
	сонячних батарей і мікро-інверторів, прикріплених до окремих панелей сонячних батарей, тому можна орієнтувати цей пункт краще.</p>

<h3>Моніторинг та усунення несправностей</h3>

<p>Майже всі інвертори мають певний рівень моніторингу та пошуку несправностей, проте, можна побачити комбінований вихід
	з кожної панелі сонячних батарей в рядку серії. Однак, мікро-інвертор може контролювати кожну панель сонячних батарей
	окремо, що дозволяє легко визначити точно, що відбувається швидше.</p>

<h3>Надмірність</h3>

<p>Якщо ваш SERIES STRING інвертор виявить недолік, вся сонячна батарея перестає виробляти енергію, поки він не буде усунений.
	Якщо мікро- інвертор виявляє несправності, інші Micro Інвертори можуть продовжувати працювати, тому ви повинні мати більш
	надійну систему.</p>

<h3>Модульність</h3>

<p>SERIES STRING INVERTERS можуть приймати тільки певну кількість панелей сонячних батарей на інверторі,
	так що не завжди можна пізніше просто додати кілька панелей.
	Хоча,  AC Панелі сонячних батарей, можуть додаватись набагато легше, тому що вони не залежать один від одного.</p>

<h3>Триваліша гарантія</h3>

<p>Так, як мікро-інвертори не зазнають таких високих електричних і теплових навантажень в якості центрального інвертора,
	вони  мають тенденцію тривати значно довше. Мікро-перетворювачі зазвичай поставляються з гарантією 20-25 років - 15-20 років довше,
	ніж центральні інвертори.</p>

<h3>Відстеження точки максимальної потужності(MPPT)</h3>

<p>Одна зі складних речей про сонячних батарей є те, що напруга повинна бути відрегульована до рівня освітленості для максимальної
	вихідної потужності. Іншими словами, продуктивність панелі сонячних батарей залежить від навантаження напруги, що подається від
	інвертора. MPPT це метод, який використовується, щоб знайти правильну напруга - точку максимальної потужності.
	MPPT застосовується  до кожної окремої панелі, на відміну від Сонячної системи в цілому, тому і продуктивність
	буде природним чином зростати.</p>

<h3>Звіти про виконання</h3>

